package org.example.FCAI;

import java.awt.Color;

class ColoredRectangle {

  int x, y, width, height;
  Color color;

  public ColoredRectangle(int x, int y, int width, int height, Color color) {
    this.x = x;
    this.y = y;
    this.width = width;
    this.height = height;
    this.color = color;
  }
}