package org.example;

import java.awt.Color;

public class ColoredRectangle {

  public int x, y, width, height;
  public Color color;

  public ColoredRectangle(int x, int y, int width, int height, Color color) {
    this.x = x;
    this.y = y;
    this.width = width;
    this.height = height;
    this.color = color;
  }
}